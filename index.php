            <div class="col-md-11" style="margin-left: 30px;">
                <h2>RA no mercado de trabalho</h2>
                <p style="text-align: justify; color: white; opacity: .9; font-weight: lighter;">O mundo dos negócios está sempre entre os primeiros a adotar as tecnologias mais recentes. Isso não é nenhuma surpresa: a competição em cada setor e nicho força os empreendedores e os CEOs a pensar como os melhores jogadores de xadrez e manter seus olhos sempre treinados sobre o que o futuro pode trazer. Neste momento, a realidade aumentada e a realidade virtual são os alvos à vista.
                    Este é um campo tão novo que <a href="https://www.burning-glass.com/blog/augmented-reality-virtual-reality-jobs-become-real/">nenhum dos cinco principais empregadores em 2017 solicitou habilidades AR ou VR antes de 2013.</a></p>
                    <br>
                </div>

                <div class="col-md-8" style="margin-left: 240px;">
                    <img src="images/AR_VR_Employers_624x270.png" style="opacity: .8;" alt="" class="img-responsive">
                </div>

                <div class="col-md-11" style="margin-left: 30px;">
                    <br>
                    <p style="text-align: justify; color: white; opacity: .9; font-weight: lighter;">Entre os empregadores, a maior demanda é proveniente do Facebook. A Accenture também está aumentando a sua contratação para RA e RV. A contratação da empresa de consultoria para RA e RV aumentou mais de 300% nos últimos dois anos. De longe, o principal papel que envolve RA e RV é o desenvolvedor de software/engenheiro, seguido pelo desenvolvedor de aplicativos móveis, engenheiros de sistemas de computação/arquitetos e outras funções. Ao considerar uma carreira na área, existem alguns pontos que você deve levar em consideração: </p>

                    <li style="font-weight: bold;">Há uma escassez de candidatos no mercado</li>
                    <p style="text-align: justify; color: white; opacity: .9; font-weight: lighter;">
                        Historicamente, os desenvolvedores de software para RV, e agora RA, vieram da indústria de games. Esta pequena indústria já está se fragmentando com a adição dessas plataformas. A demanda na indústria de jogos para Unity, Unreal e outros programadores de motores sempre foi alta. Para compor essa demanda, novas empresas lutando para trazer candidatos para RA e RV criam uma escassez de talentos; tornando a contratação de tudo isso mais difícil.
                    </p>

                    <li style="font-weight: bold;">Candidatos de qualidade e talentosos já estão colocados</li>
                    <p style="text-align: justify; color: white; opacity: .9; font-weight: lighter;">
                        Como é frequentemente o caso em uma pequena indústria nascente, os maiores talentos estão amarrados nas empresas que têm orçamentos enormes. Isso pode dificultar que startups ou empresas menores tragam candidatos de qualidade, pois a oferta das grandes corporações é difícil de competir.
                    </p>

                    <li style="font-weight: bold;">Falta de mercado de empregos centralizado</li>
                    <p style="text-align: justify; color: white; opacity: .9; font-weight: lighter;">
                        É difícil para os candidatos também - tentar encontrar emprego em qualquer realidade é igualmente difícil, já que não há lugar central para encontrar novos empregos. Sites de trabalho típicos como LinkedIn, Indeed e Monster existem, mas não são completos. No entanto, as alternativas estão espalhadas pela Internet com UploadVR, AngelList e outros sites que podem ter uma pequena seção para trabalhos. Portanto, encontrar os melhores empregos como candidatos exige muito tempo e esforço.
                    </p>

                    <li style="font-weight: bold;">Tipos de empregos</li>
                    <p style="text-align: justify; color: white; opacity: .9; font-weight: lighter;">
                        Como foi dito, o desenvolvimento aparece com destaque no mercado de trabalho de RA. Mas há muitos mais papéis no negócio de RA que precisam de pessoas. Os desenvolvedores normalmente colaboram estreitamente com designers de software e artistas 3D, bem como arquitetos e engenheiros de projeto que planejam e criam o hardware no qual o software RA é executado. Os engenheiros de validação do sistema testam esses sistemas e ajudam a resolver problemas técnicos e fazem um círculo com os desenvolvedores para garantir que os aplicativos sejam modificados de acordo.
                        Os gerentes de projeto - outra área de trabalho importante na RA - coordenam e supervisionam equipes de desenvolvimento inteiras, interagem com outras unidades de negócios e trabalham com os clientes (se os projetos não forem puramente internos). Depois, há todo um braço de marketing, vendas e distribuição que leva os produtos RA ao mercado e, eventualmente, os coloca nas mãos dos clientes (ou em torno de suas cabeças).
                    </p>
                    <li style="font-weight: bold;">Qual o grau de formação necessário?</li>
                    <p style="text-align: justify; color: white; opacity: .9; font-weight: lighter;">

                        Como tantas posições diferentes de RA estão disponíveis, vamos nos concentrar no desenvolvimento e no gerenciamento. Os empregadores estão à procura de desenvolvedores de RA com dois a três anos de experiência no desenvolvimento de aplicativos responsivos, especialmente em 3D e/ou desenvolvimento de animação, geralmente dentro das equipes de engenharia do Agile Scrum. Alguns empregadores também buscam desenvolvedores com habilidades de design.
                        Você precisará de proficiência moderada a avançada com pelo menos uma plataforma RA, como Unity, ou produtos emergentes. Você também deve ser altamente qualificado em C#, C++, Java ou em uma linguagem semelhante. Dependendo do projeto, você também pode precisar de um conhecimento em <i>motion capture</i>, reconhecimento de gestos, redes, matemática e física simulada. Você freqüentemente verá um diploma de bacharel em ciência da computação, engenharia de software ou equivalente como uma qualificação preferida ou exigida para tais cargos. <br>
                        O gerente de projeto RA (também conhecido como líder de projeto, líder de inovação ou gerente de visão computacional) deve ter ampla experiência em trabalhar em projetos de RA, muitas vezes como desenvolvedor ou engenheiro que decidiu saltar para a linha de gerenciamento. Os empregadores procuram pessoas com uma sólida experiência na supervisão de projetos de tecnologia em geral ao longo do ciclo de vida de desenvolvimento, desde o início até um produto viável. Eles também procuram pessoas com amplo conhecimento de tecnologias emergentes, tendências e melhores práticas do setor. <br>
                        Um diploma de bacharel em engenharia, ciência da computação ou uma disciplina relacionada é geralmente necessário, juntamente com pelo menos cinco anos de experiência profissional. Como é o caso de todos os cargos de gerenciamento de projetos, são necessárias fortes habilidades de comunicação verbal e escrita, assim como fortes capacidades de liderança.
                    </p>
                    <li style="font-weight: bold;">Passos iniciais</li>
                    <p style="text-align: justify; color: white; opacity: .9; font-weight: lighter; ">
                        Você tem habilidades de programação, mas não sabe por onde começar? Considere iniciar aprendendo Unity. A documentação da empresa está disponível gratuitamente, e você pode passar por um conjunto de tutoriais gratuitos e treinamento ao vivo em casa. Você provavelmente terá que investir em alguns equipamentos, como um headset para RA.
                        Em seguida, pratique até se sentir confortável com o desenvolvimento de RA e crie um portfólio de projetos que mostre seu trabalho.
                    </p>

                    <li style="font-weight: bold;">Quem está contratando?</li>
